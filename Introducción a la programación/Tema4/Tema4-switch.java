//Switch
    public static void main(String[] args) {

        String estacion = "a";
        switch (estacion) {
            case "Primavera":
                System.out.println("la estacion es "+ estacion);
                break;
            case "Verano":
                System.out.println("la estacion es "+ estacion);
                break;
            case "Otono":
                System.out.println("la estacion es "+ estacion);
                break;
            case "Invierno":
                System.out.println("la estacion es "+ estacion);
                break;
            default:
                System.out.println("No es una estacion");
                break;
        }
    }
