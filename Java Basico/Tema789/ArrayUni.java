package JavaBasicTheme.theme789;

public class ArrayUni {
    
    public static void main(String[] args) {
        
        String [] name = new String[4];
        
        name[0] = "Romel";
        name[1] = "Ernesto";
        name[2] = "Tejada";
        name[3] = "Pena";
        
        for (int i = 0; i < name.length; i++) {
            System.out.println(name[i]);
        }
        
    }
}
